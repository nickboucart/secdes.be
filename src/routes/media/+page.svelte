		<p class="title">OpenAPI as a Security Tool</p>
		<p>
			<strong>Date:</strong> 15/05/2025
		</p>
		<ul>
			<li>
				OpenAPI specifications are more than just documentation—they can be a powerful foundation for improving your 
				application's security. This talk explores how to effectively use OpenAPI in both code-first and spec-first
				workflows. We’ll discuss how well-crafted specs help uncover security issues, guide audits, and power security
				tools for testing, automated attacks, and even runtime protection. You'll walk away with practical insights 
				into turning your API specs into a security asset, not just a developer convenience.
				[<a href="https://secdes.pieterp.be/2025-05-15-OpenAPI.mp4" target="_blank">video</a>]
			</li>
