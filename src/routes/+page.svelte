			<p>
				The SecDes project (in full By-Design Cybersecure Digital Products) focuses on companies
				that are building SaaS solutions. These companies, often small or medium-sized, are
				increasingly using complex software architectures within their products. Integrating
				effective security measures within these complex architectures is a difficult task. The
				risks of incidents and data breaches are real.
			</p>
			<p class="mt-3">
				At the same time, many SaaS companies are feeling increasing pressure from the market,
				driven by legislation such as NIS2 and the European Cyber Resilience Act to demonstrate that
				their software is secure enough to manage their customers' often critical data.
			</p>
			<p class="mt-3">
				The goal of this project, initiated by KULeuven DistriNet and SIRRIS and supported by VLAIO,
				is to help SaaS companies improve the security of their digital products, even if they have
				limited cybersecurity experience.
			</p>
		</section>
