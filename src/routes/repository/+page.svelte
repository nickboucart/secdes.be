			<li><strong>By-design Cybersecure Digital Products</strong> &mdash; This presentation introduces the SECDES project and highlights
				why integrating security into the DevOps pipeline has become increasingly crucial as development cycles accelerate. It explains
				how modern teams face pressure to deliver quickly while maintaining robust protection against emerging threats. It focuses on
				three key areas: securing the software design phase by building security principles into architecture decisions from the start,
				implementing automated security testing to catch vulnerabilities early and continuously, and strengthening cybersecurity 
				governance to ensure compliance and accountability across the development process.
				[<a href="/content/2024-05-30_IntroToSECDES.pdf" target="_blank">download</a>]</li>
