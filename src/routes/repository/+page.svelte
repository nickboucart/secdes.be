			<li><strong>Security &amp; Privacy Architecture through Risk-driven Threat Assessment</strong> &mdash; When building software 
				systems, you need to continuously consider the system's security and privacy (S&amp;P) aspects. Even after the system is put 
				into production, regular S&P reassessments are key, as the system, environment and implemented security measures may evolve
				 over time. While contemporary threat modeling activities can help, they are restricted to a fleeting snapshot of the system
