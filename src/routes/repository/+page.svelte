		<ul>
			<li><strong>Softwarebeveiliging meten met OWASP SAMM</strong> &mdash; In a landscape where cyberattacks and regulation intensify,
				measuring software security becomes a strategic necessity. This article presents the OWASP SAMM framework as a practical, adaptable
				tool for assessing the maturity of an organization's security practices in software development. By evaluating governance, design,
				implementation, verification, and operations across defined maturity levels, SAMM helps organizations identify strengths and 
				improvement points in their security posture.
				[<a href="https://cybersecurity-bites.be/cybersecuritybydesign/softwarebeveiliging-meten-met-owasp-samm/" target="_blank">view online</a>]</li>
		</ul>
		<ul>
			<li><strong>Strategieën voor softwarebeveiliging in de ontwikkelingscyclus</strong> &mdash; Software development strategies can differ widely 
				in how they approach security, and this article explores three common models for embedding it into the development cycle. DevOpsSec 
				integrates security measures later in the pipeline, while DevSecOps weaves checks and controls throughout each phase, 
				and SecDevOps treats security as a foundational design principle from the very start. Each approach comes with distinct benefits and 
				challenges, making the right choice dependent on an organization's culture, processes, and technical maturity.
				[<a href="https://cybersecurity-bites.be/cybersecuritybydesign/strategieen-voor-softwarebeveiliging-in-de-ontwikkelingscyclus/" target="_blank">view online</a>]</li>
		</ul>
		<ul>
			<li><strong>An Introduction to Threat Modeling</strong> &mdash; This presentation introduces 'threat modeling', one of the core techniques in the secure software
			development lifecycle. It sheds light on what threat modeling entails, where it fits in the SDLC, what benefits
			it brings, and how to get started. The presentation also incorporates findings from a recent research project that
			investigates the current state of practice in large Dutch organizations regarding threat modeling, and
			shares their lessons learned. 
			[<a href="/content/2024-09-13_Intro_to_Threat modeling.pdf" target="_blank">download</a>]</li>
		</ul>
		<ul>
			<li><strong>SecDevOps: Beveiliging als cruciaal deel van het ontwikkelingsproces</strong> &mdash; This article makes the case for SecDevOps
				as a development philosophy in which security is not an added layer, but the very foundation of software creation. Instead of pushing security 
				tasks to later stages, SecDevOps embeds continuous security controls and accountability at every step, ensuring that critical vulnerabilities 
				are identified and addressed early.
				[<a href="https://cybersecurity-bites.be/cybersecuritybydesign/secdevops-beveiliging-als-cruciaal-deel-van-het-ontwikkelingsproces/" target="_blank">view online</a>]</li>
		</ul>
		<ul>
			<li><strong>De 4 belangrijkste SecDevOps-uitdagingen aangepakt</strong> &mdash; This article tackles the four major challenges companies often face
				when shifting to SecDevOps from culture clashes and skill gaps to tool complexity and talent shortages. It highlights the friction between
				traditional team silos and a unified security mindset, underscores the need to upskill developers, and addresses the strain on limited 
				cybersecurity resources. At the same time, it warns of tool proliferation creating noise and integration difficulties, and suggests remedies
				like automation, consolidation, and tooling alignment. Adopting SecDevOps is not only a technical shift but also an organizational journey that
				requires deliberate strategies and persistent effort.
				[<a href="https://cybersecurity-bites.be/cybersecuritybydesign/de-4-belangrijkste-secdevops-uitdagingen-aangepakt/" target="_blank">view online</a>]</li>
		</ul>
		<ul>
			<li><strong>Leveraging Product Management to Shift Left in Small SaaS Teams</strong> &mdash; Historically, security wasn't 
				always a top priority for most small SaaS teams, feature development was. Upcoming legislation
				like NIS2, CRA, ... is increasing the pressure on the teams, not from the legislation itself, but from corporate
				customers who are becoming much more demanding. In this presentation we will argue that integrating security 
				requirements early ("shifting left") should be driven by product management, as they have the best understanding
				of customer concerns from both feature and security perspectives. This understanding can drive investment in
				application security (appsec) and prioritize it on the roadmap. Product managers typically know the value of 
				the data managed by the SaaS, whether it is crucial or peripheral to customers.
				[<a href="https://www.slideshare.net/slideshow/leveraging-product-management-to-shift-left-in-small-saas-teams/272491879" target="_blank">view online</a>]</li>
		</ul>
		<ul>
			<li><strong>Keeping pace with OAuth's Evolving Security Practices</strong> &mdash; OAuth 2.0, introduced in 2012,
				is now the de facto standard for API authorization. Over time, its security
				guidance has evolved to address new threats and use cases. This presentation covers the latest OAuth 2.0 security
				best practices and highlights upcoming changes to the standard, ensuring you stay ahead in securing your
				applications. [<a href="/content/2024-10-18_Keeping_Pace_with_OAuth.pdf" target="_blank">download</a>]</li>
		</ul>
		<ul>
			<li><strong>Automated Security Testing</strong> &mdash; Automated security testing takes center stage as a critical enabler
				for secure, fast-moving software delivery. The presentation explores how security checks can be woven into every 
				stage of the DevOps pipeline, turning testing from a slow, manual afterthought into a continuous, automated safeguard. 
				It examines the power of static and dynamic testing, cloud and infrastructure scanning, and intelligent triage to focus
				teams on the issues that truly matter. By shifting left and integrating these capabilities directly into development 
				workflows, the presentation shows how to catch vulnerabilities early, reduce noise, and maintain both speed and security.
				[<a href="/content/2024-11-14_Aikido.pdf" target="_blank">download</a>]</li>
		</ul>
		<ul>
			<li><strong>Software supply chain security, NIS2 and SBOM</strong> &mdash; Software supply chain security is introduced as a
				growing priority for organizations facing complex ecosystems, new regulations, and high-impact vulnerabilities. This 
				presentation explores recent supply chain incidents like SolarWinds and XZ backdoor as a wake-up call for proactive risk 
				management. It lays out the building blocks for stronger resilience: clear policies, supplier requirements aligned with 
				NIS2, and systematic use of Software Bills of Materials (SBOMs) for transparency and traceability. 
				[<a href="/content/2024-11-14_SBOM.pdf" target="_blank">download</a>]</li>
