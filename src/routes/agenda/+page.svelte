  <!-- Event: Threat Modeling Workshop -->
  <div class="border-l-4 border-slate-500 pl-6 py-4 bg-gray-50 shadow-sm rounded-md">
    <h3 class="text-2xl font-bold text-slate-700 mb-1">Threat Modeling Workshop</h3>
