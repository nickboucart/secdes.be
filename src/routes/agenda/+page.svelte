  <!-- Event: NIS 2, DORA, and “the likes” -->
  <div class="border-l-4 border-teal-500 pl-6 py-4 bg-gray-50 shadow-sm rounded-md">
    <h3 class="text-2xl font-bold text-teal-700 mb-1">NIS 2, DORA, and "the likes"</h3>
    <div class="text-sm text-gray-600 mb-2">
      <p><strong>Date & Time:</strong> 13/06/2025</p>
      <p><strong>Speaker:</strong> Koen Vranckaert &amp; Eyup Kun</p>
      <p><strong>Location:</strong> Online seminar</p>
    </div>
    <p class="text-gray-800">
      To ensure adequate cybersecurity of hardware and software across the EU, the EU legislator has adopted many new regulations, including but not limited to NIS 2,
	   DORA, the AI Act and the Cybersecurity Act. However, the impact on those businesses, especially SMEs, remains to be seen and can constitute a major source of
	    uncertainty to conduct business in the EU. This seminar provides a bird's eye view of the new cybersecurity rules governing software, hardware and process design, 
		focusing especially on the needs of small and medium enterprises (SMEs).
    </p>
  </div>

  <!-- Event: OpenAPI as a Security Tool -->
  <div class="border-l-4 border-indigo-500 pl-6 py-4 bg-gray-50 shadow-sm rounded-md">
    <h3 class="text-2xl font-bold text-indigo-700 mb-1">OpenAPI as a Security Tool</h3>
    <div class="text-sm text-gray-600 mb-2">
      <p><strong>Date & Time:</strong> 15/05/2025</p>
      <p><strong>Speaker:</strong> Philippe De Ryck</p>
      <p><strong>Location:</strong> Online seminar</p>
    </div>
    <p class="text-gray-800">
      OpenAPI specifications are more than just documentation—they can be a powerful foundation for improving your application's security. This talk explores how to 
	  effectively use OpenAPI in both code-first and spec-first workflows. We’ll discuss how well-crafted specs help uncover security issues, guide audits, and power 
	  security tools for testing, automated attacks, and even runtime protection. You'll walk away with practical insights into turning your API specs into a security
	  asset, not just a developer convenience.
    </p>
  </div>

  <!-- Event: An Overview of Threat Modeling Tools -->
  <div class="border-l-4 border-yellow-500 pl-6 py-4 bg-gray-50 shadow-sm rounded-md">
