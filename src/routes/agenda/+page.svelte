    <p class="text-gray-800">
	  The upcoming Security-by-Design User Group meeting will focus on two key topics: supply chain security and software security testing. 
	  The session will explore how the NIS2 standard impacts software developers and highlight the importance of documenting and tracking dependencies through 
	  Software Bills of Materials (SBOMs), with an overview of relevant standards and tools. In the second part, Aikido will present how organizations can use 
	  the OWASP Top 10 to "shift left," integrating security earlier in the development cycle. Practical demonstrations will show how development teams can 
