  <!-- Event: Leveraging Product Management to Shift Left in Small SaaS Teams -->
  <div class="border-l-4 border-green-500 pl-6 py-4 bg-gray-50 shadow-sm rounded-md">
    <h3 class="text-2xl font-bold text-green-700 mb-1">Leveraging Product Management to Shift Left in Small SaaS Teams</h3>
    <div class="text-sm text-gray-600 mb-2">
      <p><strong>Date & Time:</strong> 10/10/2024</p>
      <p><strong>Speaker:</strong> Nick Boucart</p>
      <p><strong>Location:</strong> Online seminar</p>
    </div>
    <p class="text-gray-800">
      Historically, security wasn't always a top priority for most small SaaS teams; feature development was. Upcoming legislation like NIS2, CRA, ... is 
	  increasing the pressure on the teams, not from the legislation itself, but from corporate customers who are becoming much more demanding. In this 
	  presentation we will argue that integrating security requirements early ("shifting left") should be driven by product management, as they have the 
	  best understanding of customer concerns from both feature and security perspectives. This understanding can drive investment in application security 
	  (appsec) and prioritize it on the roadmap. Product managers typically know the value of the data managed by the SaaS, whether it is crucial or 
	  peripheral to customers.
    </p>
  </div>

  <!-- Event: An Introduction to Threat Modeling -->
  <div class="border-l-4 border-blue-500 pl-6 py-4 bg-gray-50 shadow-sm rounded-md">
