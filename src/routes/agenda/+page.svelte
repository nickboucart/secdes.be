      This talk introduces 'threat modeling', one of the core techniques in the secure software development lifecycle. It sheds light on what threat modeling 
	  entails, where it fits in the SDLC, what benefits it brings, and how to get started. The talk also incorporates findings from a recent research project
	   that investigates the current state of practice in large Dutch organizations regarding threat modeling, and shares their lessons learned.
    </p>
  </div>

